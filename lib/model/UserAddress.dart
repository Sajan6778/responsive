class UserAddress {
  final String streetAddress;
  final String city;
  final String state;
  final String country;
  final String postalCode;
  final int addressId;

  UserAddress({
    required this.streetAddress,
    required this.city,
    required this.state,
    required this.country,
    required this.postalCode,
    required this.addressId,
  });
}